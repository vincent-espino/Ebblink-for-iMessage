    self.message.URL = url; // Adds the URL to the message objcet.
    NSLog(@"message.URL is: %@", self.message.URL);
    MSConversation* mc = self.activeConversation;
    [mc insertMessage: self.message completionHandler: ^(NSError* error) {
        if(error != nil) {
            NSLog(@"%@", error);
            return;
        }
    }];
    [self dismiss];
}

